package Misc;

public class ConsoleColors {
    // Цвета текста
    public static final String RESET = "\u001B[0m"; // Сброс цвета
    public static final String WHITE = "\u001B[37m"; // Белый текст
    public static final String GREEN = "\u001B[32m"; // Зеленый текст
    public static final String YELLOW = "\u001B[33m"; // Желтый текст
    public static final String BLUE = "\u001B[34m"; // Синий текст
    public static final String RED = "\u001B[31m"; // Красный текст (добавлено)
}